package model;

public class DiseaseStatusBean {

	private String diseaseStatus;
	private String relapseDisease;
	private String otherDisease;

	public String getDiseaseStatus() {
		return diseaseStatus;
	}

	public void setDiseaseStatus(String diseaseStatus) {
		this.diseaseStatus = diseaseStatus;
	}

	public String getRelapseDisease() {
		return relapseDisease;
	}

	public void setRelapseDisease(String relapseDisease) {
		this.relapseDisease = relapseDisease;
	}

	public String getOtherDisease() {
		return otherDisease;
	}

	public void setOtherDisease(String otherDisease) {
		this.otherDisease = otherDisease;
	}

}
